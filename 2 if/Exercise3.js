function oddEven(input) {
    num = Number(input[0]);
    if (num % 2 == 0) {
        console.log("even");
    } else {
        console.log("odd");
    }
}

oddEven("4")

// За четни и нечетни се прави функация, която проверява дали числото е делимо на 2. Ако е = четно, ако не е = нечетно!